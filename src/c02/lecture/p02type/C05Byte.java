package c02.lecture.p02type;

public class C05Byte {
    public static void main(String[] args) {
        // 1byte = 1bit
        // -128 ~ 127
        // 1000 0000 ~ 0111 1111

        byte a = 127;
        byte b = -128;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
